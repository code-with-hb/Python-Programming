#Adding whitespace to string with tabs or newlines
print("Hemanta")
#to add a newline in a string, use the charecter combination \n:
print("Name: Hemanta\nDoB: 17-02-2000\nDist: Biswanath\nCity: Gohpur")
#to add a tab to text, we use the charecter combination \t:
print("\tName: Hemanta, DoB: 17-02-2000, Dist: Biswanath, City: Gohpur")
#we can also combines tabs and newlines in a single strings
print("Name: Hemanta\n\tDoB: 17-02-2000\n\t\tDist: Biswanath\n\t\tCity: Gohpur")